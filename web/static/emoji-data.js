// Emoji search functionality
function searchEmojis(query) {
  const results = [];
  const lowerQuery = query.toLowerCase();

  for (const [category, emojis] of Object.entries(emojiData)) {
    if (category === 'recent') continue;

    emojis.forEach(emoji => {
      // Simple search - could be enhanced with emoji names/descriptions
      if (emoji.includes(lowerQuery)) {
        results.push({
          emoji: emoji,
          category: category
        });
      }
    });
  }

  return results.slice(0, 50); // Limit results
}

// Add emoji to recent list
function addToRecent(emoji) {
  const recent = emojiData.recent;
  const index = recent.indexOf(emoji);

  if (index > -1) {
    recent.splice(index, 1);
  }

  recent.unshift(emoji);

  // Keep only last 20 recent emojis
  if (recent.length > 20) {
    recent.splice(20);
  }

  // Save to localStorage
  localStorage.setItem('recentEmojis', JSON.stringify(recent));
}

// Load recent emojis from localStorage
function loadRecentEmojis() {
  const stored = localStorage.getItem('recentEmojis');
  if (stored) {
    emojiData.recent = JSON.parse(stored);
  }
}

